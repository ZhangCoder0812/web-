/* 
  
  模块化编程发展始：
       js本身是弱化命名空间概念的，只有 全局作用域 ，函数私有作用域，块级作用域。
       而模块化开发，就是强化了命名空间的概念。

  模块化：将一个复杂的程序分封装成几个文件，并组合在一起，
          文件内部数据是私有的，暴露给全局使用。

  优点：        
      - 有利于代码分离，解构，复用。低耦合高内聚
      - 避免命名冲突
      - 相互引用，按需加载   


  设计模式就是一种思想，用来解决某一类问题。
  
  "单例设计模式" ，解决模块之前相互独立 避免变量污染 相互调用 

*/


